# Load in turtle graphics functions
from turtle import *

# Fill the square
color = "blue"
fillcolor(color)

# Commands to draw a square
begin_fill()
forward(100)
left(90)
forward(100)
left(90)
forward(100)
left(90)
forward(100)
left(90)
end_fill()

# Keep screen up until it is clicked
exitonclick()
